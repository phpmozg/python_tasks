print('Сейчас мы посчитаем сумму, разность и произведение чисел "a" и "b"')
print('Введите пожалуйста число "a":')
a = float(input())
print('Введите пожалуйста число "b":')
b = float(input())
print('+--------------------------------------------------------+')
print('| Сумма чисел "a" и "b" равна', a+b)
print('| Разность чисел "a" и "b" равна', a-b)
print('| Произведение чисел "a" и "b" равно', a*b)
print('+--------------------------------------------------------+')